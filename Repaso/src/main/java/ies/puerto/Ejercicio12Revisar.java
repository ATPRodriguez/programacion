package ies.puerto;

/**
 * Desarrolla un programa que declare una variable de tipo double y otra de tipo float,
 * y luego imprima su diferencia.
 */
public class Ejercicio12Revisar {
    public static void main(String[] args) {
        double numero1 = 5;
        float numero2 = 5f;
        System.out.println(numero1 - numero2);
    }
}
